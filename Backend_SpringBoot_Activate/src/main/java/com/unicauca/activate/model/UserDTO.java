package com.unicauca.activate.model;

import java.util.List;
import java.util.Set;

/**
 *
 * @author Paula
 */
public class UserDTO {
    private Long id;
    private String name;
    private String lastName;
    private String email;
    private String password;
    private List<Event> events;
    private List<Comment> comments;
    Set<EventUser> assistences;
    private List<Follow> followers; //seguidores
    private List<Follow> following;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getLastName() {
        return lastName;
    }

    public void setLastName(String lastName) {
        this.lastName = lastName;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public List<Event> getEvents() {
        return events;
    }

    public void setEvents(List<Event> events) {
        this.events = events;
    }

    public List<Comment> getComments() {
        return comments;
    }

    public void setComments(List<Comment> comments) {
        this.comments = comments;
    }

    public Set<EventUser> getAssistences() {
        return assistences;
    }

    public void setAssistences(Set<EventUser> assistences) {
        this.assistences = assistences;
    }

    public List<Follow> getFollowers() {
        return followers;
    }

    public void setFollowers(List<Follow> followers) {
        this.followers = followers;
    }

    public List<Follow> getFollowing() {
        return following;
    }

    public void setFollowing(List<Follow> following) {
        this.following = following;
    }
    
    
}
